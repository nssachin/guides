package com.example.restclient.entity;

public class Rates {
    private float GBP;
    private float INR;
    private float USD;

    public float getGBP() {
        return GBP;
    }

    public void setGBP(float GBP) {
        this.GBP = GBP;
    }

    public float getINR() {
        return INR;
    }

    public void setINR(float INR) {
        this.INR = INR;
    }

    public float getUSD() {
        return USD;
    }

    public void setUSD(float USD) {
        this.USD = USD;
    }
}
